const normalizeText = (value) => value.replace(/\s+/g, " ").trim();
const translatableNodes = [];
const textWalker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
  acceptNode(node) {
    const text = normalizeText(node.nodeValue);
    return translations[text] ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
  }
});

while (textWalker.nextNode()) {
  const node = textWalker.currentNode;
  const pt = normalizeText(node.nodeValue);
  translatableNodes.push({ node, pt, en: translations[pt] });
}

const languageToggle = document.querySelector(".language-toggle");
const pageTitlePt = document.title;
const pageDescription = document.querySelector('meta[name="description"]');
const pageDescriptionPt = pageDescription?.content || "";

function applyLanguage(language) {
  const english = language === "en";

  translatableNodes.forEach(({ node, pt, en }) => {
    const leading = node.nodeValue.match(/^\s*/)?.[0] || "";
    const trailing = node.nodeValue.match(/\s*$/)?.[0] || "";
    node.nodeValue = `${leading}${english ? en : pt}${trailing}`;
  });

  document.documentElement.lang = english ? "en" : "pt-BR";
  document.title = english ? pageTitlePt.replace("Portfólio", "Portfolio") : pageTitlePt;
  if (pageDescription && pageDescriptionPt) {
    pageDescription.content = english
      ? "Software engineering portfolio featuring web systems, automation, integrations, and production-ready modules."
      : pageDescriptionPt;
  }
  if (languageToggle) {
    languageToggle.textContent = english ? "PT" : "EN";
    languageToggle.setAttribute("aria-label", english ? "Mudar para português" : "Switch to English");
  }
}

const savedLanguage = localStorage.getItem("portfolio-language") === "en" ? "en" : "pt";
applyLanguage(savedLanguage);

languageToggle?.addEventListener("click", () => {
  const language = document.documentElement.lang === "en" ? "pt" : "en";
  localStorage.setItem("portfolio-language", language);
  applyLanguage(language);
});

const filters = document.querySelectorAll(".filter");
const shots = document.querySelectorAll(".shot");
const lightbox = document.getElementById("lightbox");
const lightboxImage = lightbox?.querySelector("img");
const lightboxTitle = lightbox?.querySelector("p");
const closeButton = lightbox?.querySelector(".lightbox-close");

filters.forEach((button) => {
  button.addEventListener("click", () => {
    const group = button.dataset.filter;

    filters.forEach((item) => item.classList.remove("active"));
    button.classList.add("active");

    shots.forEach((shot) => {
      shot.hidden = group !== "all" && shot.dataset.group !== group;
    });
  });
});

shots.forEach((shot) => {
  shot.addEventListener("click", () => {
    if (!lightbox || !lightboxImage || !lightboxTitle) return;

    lightboxImage.src = shot.dataset.img;
    lightboxImage.alt = shot.querySelector("img")?.alt || "";
    lightboxTitle.textContent = shot.dataset.title || "";
    lightbox.classList.add("open");
    lightbox.setAttribute("aria-hidden", "false");
  });
});

function closeLightbox() {
  if (!lightbox || !lightboxImage || !lightboxTitle) return;

  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
  lightboxImage.src = "";
  lightboxTitle.textContent = "";
}

closeButton?.addEventListener("click", closeLightbox);

lightbox?.addEventListener("click", (event) => {
  if (event.target === lightbox) closeLightbox();
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") closeLightbox();
});
